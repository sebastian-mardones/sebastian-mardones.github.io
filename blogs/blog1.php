                <img class="img1" src="../img/germinacion.jpg" alt="">
                <h2 class="title2">Como germinar semillas de marihuana en tierra</h2>
                <div class="text">                
                  <p>Una de las grandes ventajas de germinar semillas de cannabis en tierra, es que se desarrollarán en su medio desde el primer día. Una vez la semilla se abra, la raíz comenzará a expandirse por el sustrato.</p>
                  <p>Además, no tendremos que manipularlas, minimizando así el riesgo a que sufran alguna rotura como sucede con todos los demás métodos.</p>
                </div>
                <h2 class="title2">¿Qué necesitamos para germinar semillas de marihuana en tierra?</h2>
                <div class="text"> 
                  <ul>
                    <li><b>Sustrato.</b> Y apostaremos por un buen sustrato, compostado y preferiblemente esterilizado. Nos evitaremos que las semillas puedan ser atacadas por plagas, hongos, o que tengan que competir con malas hierbas.</li>
                    <li><b>Contenedor.</b> Que ya va en los gustos del cultivador. Puede ser un pequeño vasito de plástico, su maceta definitiva, o un tamaño intermedio.</li>
                    <li><b>Semillas.</b> Como ya comentamos, optaremos siempre por semillas frescas o bien conservadas. Como no veremos si han germinado hasta que la plántula asome, nos evitaremos perder unos días muy valiosos.</li>
                    <li><b>Agua.</b>Evita el agua clorada. El pH debe estar regulado entre 6.0 y 6.5. Tendremos que remojar ligeramente el sustrato, así que es buena idea hacerlo con un spray para no remover en exceso la tierra.</li>
                  </ul>
                </div>
                <img class="img2" src="../img/germinacionTierra.jpg" alt="">
                <div class="text">
                  <p>Los cultivadores que utilizan esta técnica, suelen poner en remojo las semillas de cannabis durante 12 horas previamente. Se puede hacer en un vaso con agua a temperatura ambiente.</p>
                  <p> Esto suaviza e hidrata la cáscara exterior, permitiendo una germinación más rápida. Pero se puede germinar sin este remojo previo. Tan sólo es un truco que acelerará el proceso un poco más.</p>
                  <p>Por comodidad, nosotros os recomendamos macetas pequeñas, de no más de 1 litro. Las plantas aguantarán tranquilamente varios días hasta se sea el momento del primer trasplante.</p> 
                  <p>Será más fácil llevarlas a un lugar donde se sientan cómodas, en lugar de tener que mover una gran maceta. Además se llevará un mejor control sobre el riego.</p>
                  <p>Añadimos la tierra en las macetas, sin presionar en exceso. También es interesante no llenarlas. Las semillas en los primero días tienden a estirar. Podremos ir rellenando con tierra los siguiente días para corregir ese espigamiento.</p>
